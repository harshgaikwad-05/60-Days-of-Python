# In this we will learn about the comments, Escape Sequence and Print statements.

# The comments are used to define a function or what the program or logic does

# | Escape Sequence | Meaning         |
# | --------------- | --------------- |
# | `\n`            | New line        |
# | `\t`            | Tab space       |
# | `\\`            | Backslash       |
# | `\"`            | Double quote    |
# | `\'`            | Single quote    |
# | `\r`            | Carriage return |
# | `\b`            | Backspace       |

# An escape sequence is a special character used inside strings that starts with a backslash \. It tells Python to do something special

print("print is a built-in function in Python used to show output on the screen.")

